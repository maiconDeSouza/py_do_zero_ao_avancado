import statistics

print(statistics.mean([2, 8, 10, 9, 8, 10, 7, 10, 1, 10]))

print(statistics.median([1, 2, 4, 8, 9]))
print(statistics.median([1, 2, 3, 7, 8, 9]))

print(statistics.mode([2, 8, 9, 2, 23, 1, 23, 8, 21, 23, 99, 9, 8, 5, 23]))

print(statistics.stdev([1, 1.5, 2, 2.5, 2.75, 3.25, 4.75]))
